export enum SortDirection {
  /**
   * Asc sort direction.
   */
  ASC,
  /**
   * Desc sort direction.
   */
  DESC,
  /**
   *  Asc sort direction, null/underfined first
   */
  ASC_NULL_FIRST,
  /**
   * Desc sort direction, null/underfined first
   */
  DESC_NULL_FIRST,
  /**
   *  Asc sort direction, null/underfined last
   */
  ASC_NULL_LAST,
  /**
   * Desc sort direction, null/underfined last
   */
  DESC_NULL_LAST,
}
